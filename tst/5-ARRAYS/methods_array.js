let arr = ['pedro','freitas','santos']

//PUSH => adiciona um elemento no ultimo indice do array
arr.push("gostoso")
console.log(arr)

//POP => remove o ultimo elemento do array
arr.pop()
console.log(arr)

//UNSHIT => adiciona elementos no inicio de um array
arr.unshift("oi","a")
console.log(arr)

//SHIFT => remove o primeiro elemento de um array
arr.shift()
console.log(arr)