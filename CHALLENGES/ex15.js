const classificarNumero = () => {
    let n1 = 84
    
    switch (n1) {
        case n1/1 == n1 && n1 % 2 == 0:
            console.log("Positivo e Par")
            break;
            
        case n1/1 == n1 && n1 % 2 != 0:
            console.log("Positivo e ímpar")
            break
        
        case n1/1 != n1:
            console.log("Negativo")
        
        
        case n1 == 0:
            console.log("Neutro")
        
            
        default:
            console.log("Não é nada")
    }
}

classificarNumero()